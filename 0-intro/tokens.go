package main

/*
	Tokens:
		four classes: identifiers, keywords, operators & punctuation, and literals
*/

/*
	Identifiers:
		name program entities like variables and types
		must start with a letter
*/

/*
	Keywords:
		reserved words that may not be used as identifiers
		some of them:
			break case const default else for interface select struct type
*/

/*
	Operators & punctuation:
		arithmetic operators:
			+ - * / %
		etc.
*/
